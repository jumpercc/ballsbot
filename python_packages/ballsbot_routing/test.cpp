bool EqualLines(LinearCoefficients one, LinearCoefficients two) {
    double eps = 0.01;
    if (std::abs(one.a - two.a) > eps || std::abs(one.b - two.b) > eps ||
        std::abs(one.c - two.c) > eps) {
        return false;
    }
    return true;
}


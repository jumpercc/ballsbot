bool EqualClouds(const PointCloud& one, const PointCloud& two) {
    if (one.size() != two.size()) {
        return false;
    }

    double eps = 0.01;
    for (size_t i = 0; i < one.size(); ++i) {
        if (std::abs(one[i].x - two[i].x) > eps || std::abs(one[i].y - two[i].y) > eps) {
            return false;
        }
    }

    return true;
}

